session_start();
if (isset($_POST['login'])) {
    $username = $_POST['username'];
    $password = $_POST['password'];
    $query = $connection->prepare("SELECT * FROM login WHERE usuario=:username");
    $query->bindParam("username", $username, PDO::PARAM_STR);
    $query->execute();
    $result = $query->fetch(PDO::FETCH_ASSOC);
    if (!$result) {
        header("Location: loginprueba.php");
        echo '<p class="error">Usuario incorrecto!</p>';
    } else {
        if (password_verify($password, $result['contraseña'])) {
            $_SESSION['user_name'] = $result['Nombre'];
            $_SESSION['userid'] = $result['usuario'];
            header("Location: index.php");
        } else {
            header("Location: loginprueba.php");
            echo '<p class="error">Usuario o contraseña incorrectas!</p>';
        }
    }
}
